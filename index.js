function happyHolidays(){
  return 'Happy holidays!'
}

happyHolidays()

function happyHolidaysTo(john){
  return `Happy holidays!, ${john}!`
}

happyHolidaysTo('Ramos')

function happyHolidayTo(holiday, john){
  return `Happy ${holiday}, ${john}`
}

happyHolidayTo('Happy holidays', 'Tonny')


function holidayCountdown(travel, numDays){
  return `It's ${numDays}, until ${travel}`
}

holidayCountdown(20, 'until Mother\'s Day')

